func solve(_ A: inout [Int], _ B: inout Int) -> Int {
    var ans = A[0..<B].reduce(0,+)
    var i = B - 1
    var j = A.count - 1
    var b = B
    var sum = ans
    while(b > 0) {
        sum = sum - A[i] + A[j]
        i = i - 1
        j = j - 1
        b = b - 1
        ans = max(sum, ans)
    }
    return ans
}

